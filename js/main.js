function showSkincare() {
    var skincareDiv = document.getElementById('Productslist');
    var isVisible = skincareDiv.style.display === 'block';
    skincareDiv.style.display = isVisible ? 'none' : 'block';  // Toggle visibility
}

document.addEventListener("DOMContentLoaded", function() {
  var skincareLink = document.querySelector('[onclick="showSkincare()"]');
  skincareLink.addEventListener('click', showSkincare);
});

